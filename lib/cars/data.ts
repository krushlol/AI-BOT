export function getCarById(id: string): Car | undefined {
  return cars.find((car) => car.id === id)
}

export function searchCars(params: {
  query?: string
  brand?: string
  bodyStyle?: string
  fuelType?: string
  minPrice?: number
  maxPrice?: number
  minHorsepower?: number
  seating?: number
}): Car[] {
  return cars.filter((car) => {
    if (params.query) {
      const q = params.query.toLowerCase()
      if (
        !car.brand.toLowerCase().includes(q) &&
        !car.model.toLowerCase().includes(q) &&
        !car.description.toLowerCase().includes(q) &&
        !car.bodyStyle.toLowerCase().includes(q) &&
        !car.fuelType.toLowerCase().includes(q)
      )
        return false
    }
    if (params.brand && car.brand.toLowerCase() !== params.brand.toLowerCase()) return false
    if (params.bodyStyle && car.bodyStyle !== params.bodyStyle) return false
    if (params.fuelType && car.fuelType !== params.fuelType) return false
    if (params.minPrice && car.basePrice < params.minPrice) return false
    if (params.maxPrice && car.basePrice > params.maxPrice) return false
    if (params.minHorsepower && car.specs.horsepower < params.minHorsepower) return false
    if (params.seating && car.specs.seating < params.seating) return false
    return true
  })
}

